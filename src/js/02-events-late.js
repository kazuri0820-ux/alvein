function checkMilestone(){
  const si=stageOf(S.pop);
  if(si>S.stage){
    for(let k=S.stage+1;k<=si;k++){
      if(!S.flags["m"+k]){
        S.flags["m"+k]=true;
        if(EVENTS["milestone"+k])pushEvent("milestone"+k);
        else chron("――村は成長し、ついに「"+STAGES[k].name+"」と呼ばれるようになった。"+STAGES[k].note,"big");
      }
    }
  }
}

